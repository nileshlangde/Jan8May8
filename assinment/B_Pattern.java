package assinment;

public class B_Pattern {
	public static void main(String[]args)
	{
	     for (int a=1;a<=4;a++)//1
	      
	     {
	          for (int b=1;b<=a;b++)
	            {	                      
	             System.out.print(" *");
	            }
	            System.out.println();
	       }    
	     for (int a=3;a>=1;a--)
	     
	     {
	          for (int c=1;c<=a;c++)
	            {	                      
	             System.out.print(" *");
	            }
	            System.out.println();
	       }    
	     for (int a=1;a<=4;a++)
	     
	     {
	          for (int b=1;b<=a;b++)
	            {	                      
	             System.out.print(" *");
	            }
	            System.out.println();
	       }    
	     for (int a=1;a<=3;a++)
		    
	     {
	          for (int c=3;c>=a;c--)
	            {	                      
	             System.out.print(" *");
	            }
	            System.out.println();
	       }    

	 }
	
}


